package com.ssmData.config.entity;

public class Victory {
    private int ID;
    private int[] HP;
    private int Feats;

    public int getID() {
        return this.ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public int[] getHP() {
        return this.HP;
    }

    public void setHP(int[] HP) {
        this.HP = HP;
    }

    public int getFeats() {
        return this.Feats;
    }

    public void setFeats(int Feats) {
        this.Feats = Feats;
    }


}