package com.ssmData.config.entity;

public class Diamondplate {
    /** ID*/
    private int ID;
    /** 钻石奖励*/
    private int Diamond;
    /** 加成*/
    private int Add;
    /** 权重*/
    private int Weight;

    public int getID() {
        return this.ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public int getDiamond() {
        return this.Diamond;
    }

    public void setDiamond(int Diamond) {
        this.Diamond = Diamond;
    }

    public int getAdd() {
        return this.Add;
    }

    public void setAdd(int Add) {
        this.Add = Add;
    }

    public int getWeight() {
        return this.Weight;
    }

    public void setWeight(int Weight) {
        this.Weight = Weight;
    }


}