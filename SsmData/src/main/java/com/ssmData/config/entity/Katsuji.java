package com.ssmData.config.entity;

public class Katsuji {
    private int ID;
    private int Gold;
    private int Jewel;
    private int[] Item;
    private int[] ItemN;

    public int getID() {
        return this.ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public int getGold() {
        return this.Gold;
    }

    public void setGold(int Gold) {
        this.Gold = Gold;
    }

    public int getJewel() {
        return this.Jewel;
    }

    public void setJewel(int Jewel) {
        this.Jewel = Jewel;
    }

    public int[] getItem() {
        return this.Item;
    }

    public void setItem(int[] Item) {
        this.Item = Item;
    }

    public int[] getItemN() {
        return this.ItemN;
    }

    public void setItemN(int[] ItemN) {
        this.ItemN = ItemN;
    }


}
