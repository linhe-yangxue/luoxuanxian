package com.ssmData.config.entity;

public class Fbtype {
    private int ID;
    private String Name;
    private int Type;
    private int Consume;
    private int Fbinitial;
    private int OpenLv;

    public int getID() {
        return this.ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getName() {
        return this.Name;
    }

    public void setName(String Name) {
        this.Name = Name;
    }

    public int getType() {
        return this.Type;
    }

    public void setType(int Type) {
        this.Type = Type;
    }

    public int getConsume() {
        return this.Consume;
    }

    public void setConsume(int Consume) {
        this.Consume = Consume;
    }

    public int getFbinitial() {
        return this.Fbinitial;
    }

    public void setFbinitial(int Fbinitial) {
        this.Fbinitial = Fbinitial;
    }

    public int getOpenLv() {
        return this.OpenLv;
    }

    public void setOpenLv(int OpenLv) {
        this.OpenLv = OpenLv;
    }


}
